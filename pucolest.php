 		<td><b>Why is LDL cholesterol considered "bad"?</b></td>
 	</tr>
 	<tr>
 		<td>When too much LDL cholesterol circulates in the blood, it can slowly build up in the walls of the arteries that feed the heart and brain. Together with other substances it can form plaque, a thick, hard deposit that can clog those arteries. This condition is known as atherosclerosis. If a clot forms and blocks a narrowed artery, it can cause a heart attack or stroke. The levels of HDL cholesterol and LDL cholesterol in the blood are measured to evaluate the risk of having a heart attack. LDL cholesterol of less than 100 mg/dL is the optimal level.  Less than 130 mg/dL is near optimal for most people.  A high LDL level (more than 160 mg/dL or 130 mg/dL or above if you have two or more risk factors for cardiovascular disease) reflects an increased risk of heart disease. That's why LDL cholesterol is often called "bad" cholesterol.</td>
