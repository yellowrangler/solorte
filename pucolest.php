 		<td><b>Why is HDL cholesterol considered "good"?</b></td>
 	</tr>
 	<tr>
 		<td>About one-third to one-fourth of blood cholesterol is carried by high-density lipoprotein (HDL). HDL cholesterol is known as the "good" cholesterol because a high level of it seems to protect against heart attack. (Low HDL cholesterol levels [less than 40 mg/dL] increase the risk for heart disease.) Medical experts think that HDL tends to carry cholesterol away from the arteries and back to the liver, where it's passed from the body. Some experts believe that HDL removes excess cholesterol from plaque in arteries, thus slowing the build-up.</td>
